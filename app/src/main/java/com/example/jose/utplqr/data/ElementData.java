public class ElementData {
    private String mTitulo;
    private String mImagenURI;
    private String mAutor;
    private String mCreacion;
    private String mUbicacion;
    private String mTecnica;
    private String mRepresenta;

    public void setTitulo(String titulo) {
        mTitulo = titulo;
    }

    public String getTitulo() {
        return mTitulo;
    }

    public void setImagenURI(String imagenURI) {
        mImagenURI = imagenURI;
    }

    public String getImagenURI() {
        return mImagenURI;
    }

    public void setAutor(String autor) {
        mAutor = autor;
    }

    public String getAutor() {
        return mAutor;
    }

    public void setCreacion(String creacion) {
        mCreacion = creacion;
    }

    public String getCreacion() {
        return mCreacion;
    }

    public void setUbicacion(String ubicacion) {
        mUbicacion = ubicacion;
    }

    public String getUbicacion() {
        return mUbicacion;
    }

    public void setTecnica(String tecnica) {
        mTecnica = tecnica;
    }

    public String getTecnica() {
        return mTecnica;
    }

    public void setRepresenta(String representa) {
        mRepresenta = representa;
    }

    public String getRepresenta() {
        return mRepresenta;
    }

}
